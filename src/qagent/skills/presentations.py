def dispatch(name: str, args: dict[str, Any]) -> str | None:
    if not isinstance(args, dict):
        args = {}

    if name == "make_markdown_deck":
        title = str(args.get("title", "") or "")
        slides = _normalize_slides(args.get("slides"))
        return make_markdown_deck(title, slides)

    if name == "make_pptx":
        title = str(args.get("title", "") or "")
        slides = _normalize_slides(args.get("slides"))
        return make_pptx(title, slides)

    if name == "list_presentations":
        return list_presentations()

    return None
